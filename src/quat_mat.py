import numpy as np
# ── Вспомогательные функции кватернионной алгебры ──────────────────────────
# Соглашение: q = [w, x, y, z], скалярная часть первой

def rotmat_to_quat(R: np.ndarray) -> np.ndarray:
    """
    Метод Шепперда — численно устойчив во всех квадрантах.
    Выбираем ветвь с максимальным знаменателем, чтобы избежать
    деления на почти-ноль.
    """
    tr = R[0,0] + R[1,1] + R[2,2]
    if tr > 0:
        s = np.sqrt(tr + 1.0) * 2       # s = 4w
        w = 0.25 * s
        x = (R[2,1] - R[1,2]) / s
        y = (R[0,2] - R[2,0]) / s
        z = (R[1,0] - R[0,1]) / s
    elif R[0,0] > R[1,1] and R[0,0] > R[2,2]:
        s = np.sqrt(1.0 + R[0,0] - R[1,1] - R[2,2]) * 2  # s = 4x
        w = (R[2,1] - R[1,2]) / s
        x = 0.25 * s
        y = (R[0,1] + R[1,0]) / s
        z = (R[0,2] + R[2,0]) / s
    elif R[1,1] > R[2,2]:
        s = np.sqrt(1.0 + R[1,1] - R[0,0] - R[2,2]) * 2  # s = 4y
        w = (R[0,2] - R[2,0]) / s
        x = (R[0,1] + R[1,0]) / s
        y = 0.25 * s
        z = (R[1,2] + R[2,1]) / s
    else:
        s = np.sqrt(1.0 + R[2,2] - R[0,0] - R[1,1]) * 2  # s = 4z
        w = (R[1,0] - R[0,1]) / s
        x = (R[0,2] + R[2,0]) / s
        y = (R[1,2] + R[2,1]) / s
        z = 0.25 * s
    return np.array([w, x, y, z])


def quat_mult(q1: np.ndarray, q2: np.ndarray) -> np.ndarray:
    """Произведение Гамильтона q1 ⊗ q2."""
    w1, x1, y1, z1 = q1
    w2, x2, y2, z2 = q2
    return np.array([
        w1*w2 - x1*x2 - y1*y2 - z1*z2,
        w1*x2 + x1*w2 + y1*z2 - z1*y2,
        w1*y2 - x1*z2 + y1*w2 + z1*x2,
        w1*z2 + x1*y2 - y1*x2 + z1*w2,
    ])


def quat_conj(q: np.ndarray) -> np.ndarray:
    """Сопряжённый кватернион = инверсия для единичного."""
    return np.array([q[0], -q[1], -q[2], -q[3]])